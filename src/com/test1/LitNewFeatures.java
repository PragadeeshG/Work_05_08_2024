package com.test1;

public class LitNewFeatures {
	private Integer sizeReduced;
	private String backwardCompatabilityVersion;
	private boolean importFromLit;
	private Integer polyfillSupport;
	private String scriptPath;
	private String scriptName;
	private Integer keyIdentifier;
	private Integer polyfillSuppForWebComponent;
	private String updatingElement;
	private Integer reactiveElement;
	private String internalProperty;
	private String nodepart;
	private String childPart;

	public LitNewFeatures() {

	}

	public LitNewFeatures(Integer sizeReduced, String backwardCompatabilityVersion, boolean importFromLit,
			Integer polyfillSupport, String scriptPath, String scriptName, Integer keyIdentifier,
			Integer polyfillSuppForWebComponent, String updatingElement, Integer reactiveElement,
			String internalProperty, String nodepart, String childPart) {
		super();
		this.sizeReduced = sizeReduced;
		this.backwardCompatabilityVersion = backwardCompatabilityVersion;
		this.importFromLit = importFromLit;
		this.polyfillSupport = polyfillSupport;
		this.scriptPath = scriptPath;
		this.scriptName = scriptName;
		this.keyIdentifier = keyIdentifier;
		this.polyfillSuppForWebComponent = polyfillSuppForWebComponent;
		this.updatingElement = updatingElement;
		this.reactiveElement = reactiveElement;
		this.internalProperty = internalProperty;
		this.nodepart = nodepart;
		this.childPart = childPart;
	}

	public Integer getSizeReduced() {
		return sizeReduced;
	}

	public void setSizeReduced(Integer sizeReduced) {
		this.sizeReduced = sizeReduced;
	}

	public String getBackwardCompatabilityVersion() {
		return backwardCompatabilityVersion;
	}

	public void setBackwardCompatabilityVersion(String backwardCompatabilityVersion) {
		this.backwardCompatabilityVersion = backwardCompatabilityVersion;
	}

	public boolean isImportFromLit() {
		return importFromLit;
	}

	public void setImportFromLit(boolean importFromLit) {
		this.importFromLit = importFromLit;
	}

	public Integer getPolyfillSupport() {
		return polyfillSupport;
	}

	public void setPolyfillSupport(Integer polyfillSupport) {
		this.polyfillSupport = polyfillSupport;
	}

	public String getScriptPath() {
		return scriptPath;
	}

	public void setScriptPath(String scriptPath) {
		this.scriptPath = scriptPath;
	}

	public String getScriptName() {
		return scriptName;
	}

	public void setScriptName(String scriptName) {
		this.scriptName = scriptName;
	}

	public Integer getKeyIdentifier() {
		return keyIdentifier;
	}

	public void setKeyIdentifier(Integer keyIdentifier) {
		this.keyIdentifier = keyIdentifier;
	}

	public Integer getPolyfillSuppForWebComponent() {
		return polyfillSuppForWebComponent;
	}

	public void setPolyfillSuppForWebComponent(Integer polyfillSuppForWebComponent) {
		this.polyfillSuppForWebComponent = polyfillSuppForWebComponent;
	}

	public String getUpdatingElement() {
		return updatingElement;
	}

	public void setUpdatingElement(String updatingElement) {
		this.updatingElement = updatingElement;
	}

	public Integer getReactiveElement() {
		return reactiveElement;
	}

	public void setReactiveElement(Integer reactiveElement) {
		this.reactiveElement = reactiveElement;
	}

	public String getInternalProperty() {
		return internalProperty;
	}

	public void setInternalProperty(String internalProperty) {
		this.internalProperty = internalProperty;
	}

	public String getNodepart() {
		return nodepart;
	}

	public void setNodepart(String nodepart) {
		this.nodepart = nodepart;
	}

	public String getChildPart() {
		return childPart;
	}

	public void setChildPart(String childPart) {
		this.childPart = childPart;
	}

}
